#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
血字的研究对话台词库 v3.15.0 修复脚本 - 第7部分
精准修复：场景五华生台词、困难模式底线触发
"""

import re

FILE_PATH = "/app/data/所有对话/主对话/维多利亚伦敦探案项目/02_核心设计/08_血字的研究_对话台词库.md"

def read_file():
    with open(FILE_PATH, 'r', encoding='utf-8') as f:
        return f.read()

def write_file(content):
    with open(FILE_PATH, 'w', encoding='utf-8') as f:
        f.write(content)

def main():
    content = read_file()
    print(f"当前文件长度: {len(content)} 字符")
    
    # ============================================================
    # 问题14：场景五入场增加华生台词（A路线）
    # 使用正则表达式匹配
    # ============================================================
    pattern1 = r'福尔摩斯（看向玩家）："来了。记住——让她说，别打断。"\s*\n\s*\n\s*\}'
    
    match1 = re.search(pattern1, content)
    if match1:
        old_text = match1.group(0)
        new_text = '''福尔摩斯（看向玩家）："来了。记住——让她说，别打断。"

    华生（低声，好奇）："福尔摩斯，你真的觉得她和案子有关？
    万一她真的就是个普通老太太，只是来领戒指的？"

    福尔摩斯（微微摇头）："普通老太太不会在这个时间、用这个地址来领戒指。
    太巧了——巧得不正常。"

    华生（似懂非懂）："好吧……我倒要看看她是什么人。"

    }'''
        content = content.replace(old_text, new_text)
        print("✓ 问题14：场景五A路线入场华生台词已添加")
    else:
        print("✗ 问题14：场景五A路线入场未找到匹配，尝试其他方式...")
        # 尝试更宽泛的匹配
        if '来了。记住——让她说，别打断。' in content:
            print("  找到了关键句子，但格式可能不同")
    
    # ============================================================
    # 问题21：困难模式底线触发机制
    # 在"正规侦探太大张旗鼓"之后、"【演出】门铃响了"之前添加
    # ============================================================
    pattern2 = r'我们需要……更灵活的方式。"\s*\n\s*\n\s*【演出】门铃响了'
    
    match2 = re.search(pattern2, content)
    if match2:
        old_text = match2.group(0)
        new_text = '''我们需要……更灵活的方式。"

    【困难模式·底线触发机制】
    （如果玩家全程未对老太婆产生任何怀疑，直接给了戒指放她走，
    且没有提交任何"有问题"的假设，则触发以下内容）

    福尔摩斯（从窗边回过头，眉头微蹙）："等等——你不觉得她有点不对劲吗？"

    【玩家选择】
    A. 你这么一说……好像是有点奇怪。我们追上去看看？→ 触发跟踪（补救机会）
    B. 有什么不对的？不就是个普通老太太吗？→ 福尔摩斯自己追出去，
       玩家错过跟踪体验，但最终仍能通过福尔摩斯的汇报得知脱逃事实

    （如果玩家选B：）
    福尔摩斯（摇头，披上外套）："不对劲……太不对劲了。你待在这里，我去看看。"
    （福尔摩斯匆匆追了出去。半小时后回来，告诉玩家老太婆脱逃了。）

    【演出】门铃响了'''
        content = content.replace(old_text, new_text)
        print("✓ 问题21：场景五困难模式底线触发机制已添加")
    else:
        print("✗ 问题21：场景五困难模式未找到匹配，尝试其他方式...")
        if '更灵活的方式' in content:
            print("  找到了关键句子，但格式可能不同")
    
    write_file(content)
    print(f"\n第7轮修改完成，文件长度: {len(content)} 字符")

if __name__ == "__main__":
    main()
